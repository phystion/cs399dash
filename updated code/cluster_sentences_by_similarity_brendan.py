"""# Useful reference

I got [advice from Claude](https://claude.ai/share/e3ae1af7-328e-4b0e-94f0-fcc7db170623) on the specific sklearn clustering code to use when you have a similarity matrix.  Below, I just attempt the first of the techniques Claude suggested, which works pretty well in this experiment.

# Turn similarities into distances

This is just a one-minus operation, because similarity is "closeness" and distance is "farness" so we need to turn it around.  Note that in some rare cases (when the similarity score of 1.0 wasn't exactly 1.0, but just very close to it within machine precision), we get 1.0 minus similarity to be a tiny negative number, which we therefore round up to zero, so the clustering algorithm won't yell at us about negative distances.
"""

import numpy as np
distances = (1 - df).to_numpy() # drop row/col headings, keep just the #s
distances = np.clip(distances, 0.0, None) # round up to zero if there were machine precision errors (tiny negative #s)
distances.min(), distances.max()

"""# Make function for clustering experiments

Here's a function that does the clustering and reports on it.  It also stores the goodness metric into a global array so we can make an elbow plot later.
"""

import sklearn as sk

def try_n_clusters(n):

    #global data_for_elbow_plot
    model = sk.cluster.AgglomerativeClustering(n_clusters=n, metric='precomputed', linkage='average')
    labels = model.fit_predict(distances)
    score = sk.metrics.silhouette_score(distances,labels,metric='precomputed')

    #data_for_elbow_plot.append((n,score))
    print('\n# clusters: ',n)
    print('Silhouette score: ',score)

    for cluster_index in range(n):
        cluster_sentences = [ sentence for (index,sentence) in enumerate(sentences) if labels[index] == cluster_index ]
        print('Cluster',cluster_index)

        # Only print a few examples to avoid excessive output
        for i, sentence in enumerate(cluster_sentences[:5]): # Print up to 5 sentences per cluster
            print('\t'+sentence)
        if len(cluster_sentences) > 5:
            print(f'\t... ({len(cluster_sentences) - 5} more sentences)')
    return labels

cluster_labels = try_n_clusters(10)
print(f"\nShape of cluster_labels: {cluster_labels.shape}")

