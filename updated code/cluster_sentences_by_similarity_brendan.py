import google.generativeai as genai
import google.colab.userdata
import time

genai.configure(api_key=google.colab.userdata.get('GOOGLE_API_KEY'))

# Initialize the model with 'gemini-flash-latest', as 'gemini-pro-latest' encountered quota issues
model = genai.GenerativeModel('gemini-flash-latest')

print("Gemini API configured and model initialized with 'gemini-flash-latest'.")

def generate_gemini_label(sample_sentences):
    prompt = f"Given the following sentences, generate a concise, descriptive label (once sentence) that captures the main theme of these sentences. Do not use any introductory phrases like 'The theme is' or 'This cluster is about'.\n\nSentences:\n{'; '.join(sample_sentences)}\n\nLabel:"

    retries = 3
    delay = 5  # seconds
    for i in range(retries):
        try:
            response = model.generate_content(prompt)
            # Extract the text from the response, handling potential empty responses
            try:
                label = response.candidates[0].content.parts[0].text.strip()
            except (AttributeError, IndexError):
                label = "" # Return empty string if no content is found
            break # Break loop if successful
        except Exception as e: # Catch a broader exception for safety, but specifically target TooManyRequests
            if "TooManyRequests" in str(e) and i < retries - 1:
                print(f"Quota exceeded, retrying in {delay} seconds...")
                time.sleep(delay)
                delay *= 2 # Exponential backoff
            else:
                print(f"Error generating label after {i+1} attempts: {e}")
                label = "" # Return empty string on persistent error
                break

    # Ensure the label is concise (max 5 words)
    # label_words = label.split()
    # if len(label_words) > 5:
    #     label = ' '.join(label_words[:5])

    return label

print("Function 'generate_gemini_label' redefined with retry logic.")

